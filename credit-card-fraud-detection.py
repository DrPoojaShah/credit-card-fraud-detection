# --------------------------------------------------
# 1. Cost assumptions
# --------------------------------------------------
C_FP = 50.0     # Cost of one false positive alert
C_FN = 500.0    # Cost of missing one fraud case


# --------------------------------------------------
# 2. BMR-inspired threshold function
# --------------------------------------------------
def bmr_threshold(amt, c_fp=C_FP):
    """
    BMR-inspired dynamic threshold.
    Higher transaction amount -> lower threshold.
    """
    return np.clip(
        c_fp / (c_fp + np.maximum(amt, 0.01)),
        0.01,
        0.99
    )


# --------------------------------------------------
# 3. Visualize threshold behavior
# --------------------------------------------------
amounts = np.logspace(0, 4, 300)  # $1 to $10,000
thresholds = bmr_threshold(amounts)

plt.figure(figsize=(9, 4))
plt.semilogx(amounts, thresholds, linewidth=2)
plt.axhline(0.5, color="gray", linestyle="--", label="Fixed 0.5 threshold")

example_amounts = np.array([5, 500, 5000])
plt.scatter(
    example_amounts,
    bmr_threshold(example_amounts),
    zorder=5,
    color="red",
    label="Example amounts"
)

for amt in example_amounts:
    t = float(bmr_threshold(np.array([amt])))
    plt.annotate(
        f"${amt}\nτ={t:.2f}",
        xy=(amt, t),
        xytext=(amt * 1.5, t + 0.05),
        fontsize=9
    )

plt.xlabel("Transaction Amount ($, log scale)")
plt.ylabel("Minimum Fraud Probability Threshold (τ)")
