import numpy as np
import pandas as pd

def haversine_vectorized(lat1, lon1, lat2, lon2):
    R = 6371.0  # km

    lat1 = np.radians(lat1)
    lon1 = np.radians(lon1)
    lat2 = np.radians(lat2)
    lon2 = np.radians(lon2)

    dlat = lat2 - lat1
    dlon = lon2 - lon1

    a = np.sin(dlat / 2.0) ** 2 + np.cos(lat1) * np.cos(lat2) * np.sin(dlon / 2.0) ** 2
    c = 2 * np.arctan2(np.sqrt(a), np.sqrt(1 - a))

    return R * c

def add_sequential_features(df):
    df = df.sort_values(["cc_num", "unix_time"]).copy()

    # previous transaction info for same card
    df["prev_unix_time"] = df.groupby("cc_num")["unix_time"].shift(1)
    df["prev_merch_lat"] = df.groupby("cc_num")["merch_lat"].shift(1)
    df["prev_merch_long"] = df.groupby("cc_num")["merch_long"].shift(1)

    # time gap
    df["time_since_prev_txn_sec"] = df["unix_time"] - df["prev_unix_time"]
    df["time_since_prev_txn_sec"] = df["time_since_prev_txn_sec"].fillna(0)

    # distance from previous transaction
    df["distance_from_prev_txn_km"] = 0.0

    mask = df["prev_merch_lat"].notna() & df["prev_merch_long"].notna()

    df.loc[mask, "distance_from_prev_txn_km"] = haversine_vectorized(
        df.loc[mask, "prev_merch_lat"],
        df.loc[mask, "prev_merch_long"],
        df.loc[mask, "merch_lat"],
        df.loc[mask, "merch_long"]
    )

    # speed = distance / time
    df["speed_from_prev_kmph"] = 0.0
    valid_time = df["time_since_prev_txn_sec"] > 0

    df.loc[valid_time, "speed_from_prev_kmph"] = (
        df.loc[valid_time, "distance_from_prev_txn_km"] /
        (df.loc[valid_time, "time_since_prev_txn_sec"] / 3600.0)
    )

    # impossible travel flag
    df["impossible_travel_flag"] = (
        (df["distance_from_prev_txn_km"] > 500) &
        (df["time_since_prev_txn_sec"] > 0) &
        (df["time_since_prev_txn_sec"] < 3600)
    ).astype(int)

    # cleanup helper cols
    df.drop(columns=["prev_unix_time", "prev_merch_lat", "prev_merch_long"], inplace=True)

    return df

